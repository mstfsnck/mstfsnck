package ch04_JavaOperators.jo02.AssiggnmentOperators;

public class C01_AtamaOperator {
    public static void main(String[] args) {

        int yas = 33 ; // create edilen yas variable ına 33 değeri atandı
        yas += 5 ; // yas 5 artırıldı ve atandı
        yas *= 2; // yas iki ile carpılıp değerine atandı
        yas /= 2 ; // yas 2 ye bolunup degerine atandı

        System.out.println("yas = " + yas); // 38


    }
}
