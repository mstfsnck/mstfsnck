public class C04_MathMethods {
    public static void main(String[] args) {

        int a = 9 ;
        System.out.println("Math.pow(a,2) = " + Math.pow(a, 2)); // Math.pow(a,2) = 81.0

        System.out.println("Math.sqrt(a) = " + Math.sqrt(a)); // Math.sqrt(a) = 3.0




    }
}
